package calculator;

public class Q01_Fa {

    public int sum(int a, int b) {
        return a + b;
    }

    public int product(int a, int b) {
        return a * b;
    }

    public int difference(int a, int b) {
        return a - b;
    }

    public int divison(int a, int b) {
        return a / b;
    }
}
